              <h3>Limitations</h3>
              <p>
                Instantly we were hit with the fact that changing functionality was going to be in issue, a lack of development resource meant that we had to focus on the design first and any functionality changes would have to be forced through if necessary. The next issue was the tight deadline. We only have a couple of months for design and development, for a complete overhaul like this it’s simply not enough time.
              </p>
              <img src="img/projects/lovell/2.jpg" alt="lovell wireframes"/>
              <p class="note">
                Initial wireframes.
              </p>

              <h3>The Solution</h3>
              <p>
                With this project rather than tone our designs back from the get go we decided to design what we thought the ideal site would be and then present it to the stakeholders, from here we would remove any elements that we thought were too excessive or took too much development time some of these elements were removed from v1 with the intention of implementing them in the future.
              </p>
              <img src="img/projects/lovell/3.jpg" alt="new lovell homepage"/>
              <p class="note">
                The new Lovell Soccer homepage
              </p>

              <h3>Project Goals</h3>
              <p>
                With most ecommerce websites the main goal for stakeholders is to positively impact conversion rates however with this particular site our main goal was to elevate the aesthetics of the platform without negatively impacting the sales of the website.
              </p>
              <p>
                We smashed this target immediately with conversions for the month following release being 15% higher than the 12 month average. Launching in October the following Black Friday was the most profitable day ever for the site, with the highest conversion rate in three years. After the new site hype died down the conversion rate levelled out but is still a significant improvement on the previous stats.
              </p>
