              <h3>The Problem</h3>
              <p>
                Lovell’s success came through their flagship rugby website, however when it came to diversifying their product by opening a soccer site rather than elevate the store to modern standards and focusing on the difference in audience a cookie cutter approach was taken and the aesthetics and functionality was taken from the existing Lovell Rugby website. As a result the soccer site was not performing to it’s full potential.
              </p>
