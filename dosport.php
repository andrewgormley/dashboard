              <h3>The Problem</h3>
              <p>
                DO Sport was going through a major rebrand. The issue was that DO confirmed its place in the market as cheap, well made running clothing. However the brand wanted to have a stronger underlying message backed by a superior range of clothing worn for functionality but more importantly style. The team and I were tasked with the entire brand launch which consisted of a completely redesigned ecommerce website as well as getting their new social channels off the ground.
              </p>
              <img src="img/projects/do/moodboard.jpg" alt="do sport moodboard"/>
              <p class="note">
                Part of the initial moodboard.
              </p>

              <h3>Limitations</h3>
              <p>
                With a deadline of 8 weeks we were instantly constrained by both Back and Front-End resources. The ecommerce side of the DO website had to use the same framework as it’s parent company Pro:Direct. This was a big issue for us since both the brand guidelines and the Director of DO deemed it necessary for the brand to look different from conventional ecommerce platforms.
              </p>
              <img src="img/projects/do/concepts.jpg" alt="do sport concept designs"/>
              <p class="note">
                Over 30 conceptswere designed for the homepage.
              </p>

              <h3>The Solution</h3>
              <p>
                As a team we came up with the idea to take advantage of the parts of the site that were not tied to the existing framework. Mainly the homepage, other landing pages and the art direction of the product photography. This allowed us to take control through the creative direction of the site.
              </p>
              <p>
                Very early on the contents of each page were decided and a sitemap was created, which left us with sufficient time to nail the designs. This was achieved through mood boarding, white board sketching and constantly running open discussions about our concept designs.
              </p>
              <img src="img/projects/do/site-map.jpg" alt="do sport site map"/>
              <p class="note">
                Early site map to gauge workload and user journeys.
              </p>

              <p>
                Once a general direction had been decided on more time was spent on a selection of concepts which we developed to either a static high fidelity design, HTML/CSS prototypes and even After Effects videos to help preview animations. At this stage we were constantly presenting our work to stakeholders, taking onboard feedback and iterating our prototypes.
              </p>
              <p>
                Sign off was a good day. The next step was to take this vision from design to working product. This was weeks of working with Front-End to perfect layouts and to ensure functionality was king. These weeks consisted a lot of sitting together and really honing down CSS and responsive behaviour. And where necessary editing designs to combat development restraints.
              </p>

              <img src="img/projects/do/product-pages.jpg" alt="do sport product pages"/>
