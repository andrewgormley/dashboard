              <h3>My Role</h3>
              <p>
                On this project I took a lead design role. At the concept stage I worked with <a href="https://www.instagram.com/_stronach/">@_stronach</a> a graphic designer specialising in branding, as well as overseeing a few junior designers. This allowed us to produce more ideas through collaboration and critiquing. Once the direction was decided I was the sole designer working with Engineering for the web build, Videography for web and social videos and Photography for product images and campaign assets.
              </p>
              <img src="img/projects/do/products.jpg" alt="do sport product photography"/>
