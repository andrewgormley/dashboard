              <h3>Conclusion</h3>
              <p>
                The brand launch was a success. Both a high volume of traffic and sales were received on launch through DO’s social channels as well as utilising Pro:Direct’s social presence. The project received great feedback from both stakeholders and customers alike, and we achieved our goal of changing the direction of the Brand. Looking back on the project I feel I could have pushed harder to have more of an influence over the use of existing frameworks which would have given us more creative licence over more of the web experience. That said, within the tight deadline we worked to I am very pleased with the results.
